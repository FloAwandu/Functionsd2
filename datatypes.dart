void main() {
  // Example of int data type
  int age = 25;
  print('Age: $age');

  // Example of double data type
  double temperature = 25.5;
  print('Temperature: $temperature');

  // Example of String data type
  String name = 'Flo Awandu';
  print('Name: $name');

  // Example of List data type (List of integers)
  List<int> numbers = [10, 21, 63, 4, 25];
  print('Numbers: $numbers');

  // Example of List data type (List of strings)
  List<String> fruits = ['Apple', 'Banana', 'Orange'];
  print('Fruits: $fruits');

  // Example of Map data type (Map of String keys and int values)
  Map<String, int> agesMap = {'John': 30, 'Alice': 25, 'Robin': 15};
  print('Ages Map: $agesMap');

  // Example of Map data type (Map of int keys and String values)
  Map<int, String> studentsMap = {1: 'John', 2: 'Alice', 3: 'Robin'};
  print('Students Map: $studentsMap');
}